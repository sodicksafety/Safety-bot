// --------------------------------------------------
// MAIN WEBHOOK
// --------------------------------------------------
app.post("/webhook", line.middleware(config), async (req, res) => {
  const event = req.body.events[0];

  if (!event || event.type !== "message" || event.message.type !== "text") {
    return res.status(200).end();
  }

  let msg = event.message.text.toLowerCase().trim().replace(/\s+/g, "");

  // --------------------------------------------------
  // 1) Emergency
  // --------------------------------------------------
  if (
    msg.includes("อุบัติเหตุ") ||
    msg.includes("ฉุกเฉิน") ||
    msg.includes("ไฟไหม้") ||
    msg.includes("บาดเจ็บ") ||
    msg.includes("danger") ||
    msg.includes("emergency")
  ) {
    return reply(event, `⚠️ เหตุฉุกเฉิน กรุณาติดต่อทันที\nโรงงาน 1: 102 / 127 / 129\nโรงงาน 2: 137\nผู้จัดการ: 100`);
  }

  // --------------------------------------------------
  // 2) Safety Q&A
  // --------------------------------------------------
  const found = safetyQA.find((q) => msg.includes(q.question.replace(/\s+/g, "")));
  if (found) return reply(event, found.answer);

  // --------------------------------------------------
  // 3) Categories — ตอบแม่นที่สุด
  // --------------------------------------------------
  for (const category in categories) {
    for (const word of categories[category]) {
      if (msg.includes(word)) {
        return reply(event, replies[category][word]);
      }
    }
  }

  // --------------------------------------------------
  // 4) Fallback — แจ้งผู้ดูแลระบบ + mention
  // --------------------------------------------------
  return client.replyMessage(event.replyToken, {
    type: "text",
    text: `ระบบยังไม่มีข้อมูลคำถามนี้ครับ 🙂

แจ้งผู้ดูแลระบบ: @Trerasak_K
เพิ่มเพื่อนผู้ดูแล: https://line.me/ti/p/_T4H-3TKUa

ศึกษาข้อมูลเพิ่มเติม:
https://drive.google.com/file/d/1mRW60fJ7BlLeh1j_3luhZjgLUiaIjrn6/view`,
    mention: {
      mentionees: [
        {
          type: "user",
          userId: "YOUR_USER_ID"   // ← ใส่ userId ของไก่ตรงนี้
        }
      ]
    }
  });
});

// --------------------------------------------------
// Helper
// --------------------------------------------------
function reply(event, text) {
  return client.replyMessage(event.replyToken, { type: "text", text });
}

